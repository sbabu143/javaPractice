package operators;

public class Comparison
{
    public static void main(String[] args) {
        int r=15;
        int p=20;
        System.out.println(r==p); //false
        System.out.println(r!=p); //true
        System.out.println(r>p);  //false
        System.out.println(r<p);  //ture
        System.out.println(r<=p);  //true
        System.out.println(r>=p);   //false

    }
}
