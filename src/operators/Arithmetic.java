package operators;

public class Arithmetic {


    public static void main(String[] args) {
        byte n=25;
        byte m=35;
        System.out.println(n+m);
        System.out.println(m-n);
        System.out.println(n*m);
        System.out.println(m/n);
        System.out.println(n%m);

    }
}
